    [Test]
    public async Task GenerateRecipeAsync_ValidResponse_ReturnsPopulatedRecipe()
    {
        var handler = new Mock<HttpMessageHandler>();
        handler
            .Protected()
            .Setup<Task<HttpResponseMessage>>(
                "SendAsync",
                ItExpr.IsAny<HttpRequestMessage>(),
                ItExpr.IsAny<CancellationToken>())
            .ReturnsAsync(new HttpResponseMessage(HttpStatusCode.OK)
            {
                Content = GeminiResponseEnvelope(ValidRecipeJson())
            });

        var sut = CreateService(handler);
        var recipe = await sut.GenerateRecipeAsync(new RecipeGenerationRequest());

        Assert.That(recipe, Is.Not.Null);
        Assert.That(recipe!.Name, Is.EqualTo("AI Test Chicken"));
        Assert.That(recipe.Description, Is.EqualTo("A test recipe generated by Gemini."));
        Assert.That(recipe.MealTypes, Is.EqualTo("dinner"));
        Assert.That(recipe.PrepTimeMinutes, Is.EqualTo(20));
        Assert.That(recipe.DefaultServings, Is.EqualTo(2));
        Assert.That(recipe.CaloriesPerServing, Is.EqualTo(450));
        Assert.That(recipe.ProteinPerServing, Is.EqualTo(40m));
        Assert.That(recipe.CarbsPerServing, Is.EqualTo(30m));
        Assert.That(recipe.FatPerServing, Is.EqualTo(15m));
        Assert.That(recipe.Ingredients, Has.Count.EqualTo(3));
        Assert.That(recipe.InstructionList, Has.Count.EqualTo(3));
        Assert.That(recipe.IsUserCreated, Is.True);
        Assert.That(recipe.Id, Is.EqualTo(-1), "LLM recipe should have temporary Id=-1");
    }

