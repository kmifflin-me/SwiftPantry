    /// <summary>Create a service backed by the given mock handler, configuration key, and feature flag.</summary>
    private static LlmRecipeService CreateService(
        Mock<HttpMessageHandler> handlerMock,
        string apiKey = "test-api-key",
        bool featureEnabled = true,
        int timeoutSeconds = 30)
    {
        var httpClient = new HttpClient(handlerMock.Object)
        {
            Timeout = TimeSpan.FromSeconds(timeoutSeconds)
        };

        var options = Options.Create(new GeminiOptions
        {
            ApiKey          = apiKey,
            Model           = "gemini-2.0-flash",
            MaxOutputTokens = 4096
        });

        var config = new ConfigurationBuilder()
            .AddInMemoryCollection(new Dictionary<string, string?>
            {
                ["Features:EnableLlmRecipes"] = featureEnabled.ToString().ToLower()
            })
            .Build();

        return new LlmRecipeService(httpClient, options, config, NullLogger<LlmRecipeService>.Instance);
    }

