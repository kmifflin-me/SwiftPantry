    // ─── Prompt construction / request body ──────────────────────────────────

    [Test]
    public async Task GenerateRecipeAsync_SendsCorrectRequestBody()
    {
        HttpRequestMessage? capturedRequest = null;

        var handler = new Mock<HttpMessageHandler>();
        handler
            .Protected()
            .Setup<Task<HttpResponseMessage>>(
                "SendAsync",
                ItExpr.IsAny<HttpRequestMessage>(),
                ItExpr.IsAny<CancellationToken>())
            .Callback<HttpRequestMessage, CancellationToken>((req, _) => capturedRequest = req)
            .ReturnsAsync(new HttpResponseMessage(HttpStatusCode.OK)
            {
                Content = GeminiResponseEnvelope(ValidRecipeJson())
            });

        var sut = CreateService(handler);
        var request = new RecipeGenerationRequest
        {
            PantryIngredients  = new List<string> { "chicken breast", "olive oil" },
            MealType           = "dinner",
            MaxPrepTimeMinutes = 30,
            DailyCalorieTarget = 2000,
            ProteinTargetGrams = 150
        };

        await sut.GenerateRecipeAsync(request);

        Assert.That(capturedRequest, Is.Not.Null);

        var bodyJson = await capturedRequest!.Content!.ReadAsStringAsync();

        // Request should include the pantry ingredients in the prompt
        Assert.That(bodyJson, Does.Contain("chicken breast"),
            "Request body should reference pantry items");

        // Request should include the meal type
        Assert.That(bodyJson, Does.Contain("dinner"),
            "Request body should reference the meal type");

        // Request should include the calorie target
        Assert.That(bodyJson, Does.Contain("2000"),
            "Request body should reference the calorie target");

        // Request should include responseMimeType = application/json
        Assert.That(bodyJson, Does.Contain("application/json"),
            "Request body should request JSON response MIME type");
    }

