    /// <summary>Build a minimal valid Gemini JSON response envelope wrapping <paramref name="innerJson"/>.</summary>
    private static StringContent GeminiResponseEnvelope(string innerJson)
    {
        // Escape the inner JSON as a JSON string value
        var escapedInner = JsonSerializer.Serialize(innerJson); // produces "\"...\""
        var envelope = $$"""
            {
              "candidates": [
                {
                  "content": {
                    "role": "model",
                    "parts": [{ "text": {{escapedInner}} }]
                  }
                }
              ]
            }
            """;
        return new StringContent(envelope, Encoding.UTF8, "application/json");
    }

