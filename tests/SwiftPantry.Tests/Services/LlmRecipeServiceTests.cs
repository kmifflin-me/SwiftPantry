    [Test]
    public async Task GenerateRecipeAsync_Timeout_ReturnsNull()
    {
        var handler = new Mock<HttpMessageHandler>();
        handler
            .Protected()
            .Setup<Task<HttpResponseMessage>>(
                "SendAsync",
                ItExpr.IsAny<HttpRequestMessage>(),
                ItExpr.IsAny<CancellationToken>())
            .Returns<HttpRequestMessage, CancellationToken>(async (_, ct) =>
            {
                // Delay longer than the 1ms timeout we configure below
                await Task.Delay(200, ct);
                return new HttpResponseMessage(HttpStatusCode.OK);
            });

        // 1ms timeout will fire before the 200ms delay
        var httpClient = new HttpClient(handler.Object) { Timeout = TimeSpan.FromMilliseconds(1) };
        var options = Options.Create(new GeminiOptions { ApiKey = "test", Model = "gemini-2.0-flash" });
        var config = new ConfigurationBuilder()
            .AddInMemoryCollection(new Dictionary<string, string?> { ["Features:EnableLlmRecipes"] = "true" })
            .Build();

        var sut = new LlmRecipeService(httpClient, options, config, NullLogger<LlmRecipeService>.Instance);
        var recipe = await sut.GenerateRecipeAsync(new RecipeGenerationRequest());

        Assert.That(recipe, Is.Null);
    }

